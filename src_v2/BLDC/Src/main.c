void motor_loop(uint8_t force){
	static uint8_t status, last_status;

	//throttle off about 1v
	if(throttle < 1){
		htim1.Instance->CCER = 0x555;
		htim1.Instance->CCR1 = 0;
		htim1.Instance->CCR2 = 0;
		htim1.Instance->CCR3 = 0;
		return;
	}
	
	if(force == 1){
		status = (status + 1) % 6;
	}else if(force == 2){
		//sens
		uint8_t sens_status =	HAL_GPIO_ReadPin(SENS_U_GPIO_Port, SENS_U_Pin) << 0 |
													HAL_GPIO_ReadPin(SENS_V_GPIO_Port, SENS_V_Pin) << 1 |
													HAL_GPIO_ReadPin(SENS_W_GPIO_Port, SENS_W_Pin) << 2;
		
		uint8_t phase_table[] = { 0, 5, 3, 4, 1, 0, 2 };
		if(sens_status > 7 || sens_status < 1){
			return;
		}
		status = phase_table[sens_status];
	}else if(force == 3){
		//hall
		uint8_t hall_status =	HAL_GPIO_ReadPin(HALL_U_GPIO_Port, HALL_U_Pin) << 0 |
													HAL_GPIO_ReadPin(HALL_V_GPIO_Port, HALL_V_Pin) << 1 |
  												HAL_GPIO_ReadPin(HALL_W_GPIO_Port, HALL_W_Pin) << 2;
		
		uint8_t phase_table[] = { 0, 5, 3, 4, 1, 0, 2 };
		if(hall_status > 7 || hall_status < 1){
			return;
		}
		status = phase_table[hall_status];
	}
	
	//if corrent?
	if( status == ((last_status + 1) % 6) ){
		//reset timeout counter
		__HAL_TIM_SET_COUNTER(&htim14, 0);
	}
	last_status = status;

	uint8_t t = throttle;

	switch(status){
		case 0://5
			htim1.Instance->CCR2 = 0;
			htim1.Instance->CCR3 = t;
			htim1.Instance->CCER = 0x550;
			break;
		case 1://4	
			htim1.Instance->CCR1 = 0;
			htim1.Instance->CCR3 = t;
			htim1.Instance->CCER = 0x505;
			break;
		case 2://6
			htim1.Instance->CCR1 = 0;
			htim1.Instance->CCR2 = t;
			htim1.Instance->CCER = 0x055;
			break;
		case 3://2
			htim1.Instance->CCR3 = 0;
			htim1.Instance->CCR2 = t;
			htim1.Instance->CCER = 0x550;
			break;
		case 4://3
			htim1.Instance->CCR3 = 0;
			htim1.Instance->CCR1 = t;
			htim1.Instance->CCER = 0x505;
			break;
		case 5://1
			htim1.Instance->CCR2 = 0;
			htim1.Instance->CCR1 = t;
			htim1.Instance->CCER = 0x055;
			break;
		}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	switch(GPIO_Pin){
		case HALL_U_Pin:
		case HALL_V_Pin:
		case HALL_W_Pin:
			motor_loop(3);
			break;
		case SENS_U_Pin:
		case SENS_V_Pin:
		case SENS_W_Pin:
			//motor_loop(2);
			break;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if(htim->Instance == htim14.Instance){
			//force change phase
			motor_loop(1);
		}else if(htim->Instance == htim17.Instance){
			//avg throttle
			static uint16_t throttle_raw;
			throttle_raw = ( throttle_raw + adc_buf[2] ) /2; //avg
			
			if(throttle_raw < 819){ // 1000mv
				throttle = 0;
			}else if(throttle_raw > 3276){ //4000mv
				throttle = 255;
			}else{
				throttle = ((throttle_raw - 819) + 1)/9.7;//9.673228346456693
			}
		}
}


