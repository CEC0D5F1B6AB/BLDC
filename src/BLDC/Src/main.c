void motor_loop(){
	uint8_t hall_status;
	
	hall_status =	HAL_GPIO_ReadPin(HALL_U_GPIO_Port, HALL_U_Pin) << 0 |
								HAL_GPIO_ReadPin(HALL_V_GPIO_Port, HALL_V_Pin) << 1 |
								HAL_GPIO_ReadPin(HALL_W_GPIO_Port, HALL_W_Pin) << 2;
	

	
	int t = 0;
	if(adc_value[4] < 500){ // 400mv
		htim1.Instance->CCER = 0;
		return;
	}else if(adc_value[4] >= 2500){
		t = 2500;
	}else{
		t = adc_value[4] - 500;
	}
	
	motor_throttle = t>>3;
	int d = motor_throttle;

	switch(hall_status){
		case 5:
			htim1.Instance->CCR2 = 314-d;
			htim1.Instance->CCR3 = 314+d;
			htim1.Instance->CCER = 0x550;
			htim1.Instance->CCR1 = 314;
			break;
		case 4:
			htim1.Instance->CCR1 = 314-d;
			htim1.Instance->CCR3 = 314+d;
			htim1.Instance->CCER = 0x505;
			htim1.Instance->CCR2 = 314;
			break;
		case 6:
			htim1.Instance->CCR1 = 314-d;
			htim1.Instance->CCR2 = 314+d;
			htim1.Instance->CCER = 0x055;
			htim1.Instance->CCR3 = 314;
			break;
		case 2:
			htim1.Instance->CCR3 = 314-d;
			htim1.Instance->CCR2 = 314+d;
			htim1.Instance->CCER = 0x550;
			htim1.Instance->CCR1 = 314;
			break;
		case 3:
			htim1.Instance->CCR3 = 314-d;
			htim1.Instance->CCR1 = 314+d;
			htim1.Instance->CCER = 0x505;
			htim1.Instance->CCR2 = 314;
			break;
		case 1:
			htim1.Instance->CCR2 = 314-d;
			htim1.Instance->CCR1 = 314+d;
			htim1.Instance->CCER = 0x055;
			htim1.Instance->CCR3 = 314;
			break;
	}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	
	switch(GPIO_Pin){
		case HALL_U_Pin:
		case HALL_V_Pin:
		case HALL_W_Pin:
			motor_loop();

			motor_rpm_raw = __HAL_TIM_GET_COUNTER(&htim14);
			//__HAL_TIM_SET_PRESCALER(&htim1, (motor_rpm_raw>>8)+8);
			__HAL_TIM_SET_COUNTER(&htim14, 0);
			break;
	}
	
	switch(GPIO_Pin){
		case HALL_U_Pin:
			HAL_GPIO_WritePin(LED_R_GPIO_Port, LED_R_Pin, HAL_GPIO_ReadPin(HALL_U_GPIO_Port, HALL_U_Pin));
			break;
		case HALL_V_Pin:
			HAL_GPIO_WritePin(LED_G_GPIO_Port, LED_G_Pin, HAL_GPIO_ReadPin(HALL_V_GPIO_Port, HALL_V_Pin));
			break;
		case HALL_W_Pin:
			HAL_GPIO_WritePin(LED_B_GPIO_Port, LED_B_Pin, HAL_GPIO_ReadPin(HALL_W_GPIO_Port, HALL_W_Pin));
			break;
	}
}


